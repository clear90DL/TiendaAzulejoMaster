    private void btnGuardarInformacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarInformacionActionPerformed
          String idEmpresaStr = txt_IDEmpresa.getText().trim();
    String nombre = txtNombreEmpresa.getText().trim();
    String direccion = txtDireccion.getText().trim();
    String email = txtEmail.getText().trim();
    String telefono = txtTelefono.getText().trim();
    String web = txtWebSite.getText().trim();
    String logo = txtImagen.getText().trim();

    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è El nombre de la empresa es obligatorio");
        return;
    }

    int idEmpresa = 1; // üëà ID fijo, puedes ignorar el txt_IDEmpresa si siempre ser√° 1

    try (Connection con = ConexionBD.getConnection()) {
        // Verificar si ya existe un registro
        String checkSql = "SELECT COUNT(*) FROM informacion_empresa WHERE id_empresa = ?";
        try (PreparedStatement checkPs = con.prepareStatement(checkSql)) {
            checkPs.setInt(1, idEmpresa);
            ResultSet rs = checkPs.executeQuery();
            rs.next();
            int count = rs.getInt(1);

            if (count == 0) {
                // üü¢ No existe ‚Üí INSERT
                String insertSql = "INSERT INTO informacion_empresa (id_empresa, nombre_empresa, direccion, email, telefono, web_site, logo_imagen) VALUES (?, ?, ?, ?, ?, ?, ?)";
                try (PreparedStatement ps = con.prepareStatement(insertSql)) {
                    ps.setInt(1, idEmpresa);
                    ps.setString(2, nombre);
                    ps.setString(3, direccion);
                    ps.setString(4, email);
                    ps.setString(5, telefono);
                    ps.setString(6, web);
                    ps.setString(7, logo);

                    int filas = ps.executeUpdate();
                    if (filas > 0) {
                        JOptionPane.showMessageDialog(this, "‚úÖ Informaci√≥n guardada correctamente.");
                    }
                }
            } else {
                // ‚úèÔ∏è Ya existe ‚Üí UPDATE
                String updateSql = "UPDATE informacion_empresa SET nombre_empresa=?, direccion=?, email=?, telefono=?, web_site=?, logo_imagen=? WHERE id_empresa=?";
                try (PreparedStatement ps = con.prepareStatement(updateSql)) {
                    ps.setString(1, nombre);
                    ps.setString(2, direccion);
                    ps.setString(3, email);
                    ps.setString(4, telefono);
                    ps.setString(5, web);
                    ps.setString(6, logo);
                    ps.setInt(7, idEmpresa);

                    int filas = ps.executeUpdate();
                    if (filas > 0) {
                        JOptionPane.showMessageDialog(this, "‚úÖ Informaci√≥n actualizada correctamente.");
                    }
                }
            }
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "‚ùå Error al guardar la informaci√≥n:\n" + e.getMessage());
    }
    }//GEN-LAST:event_btnGuardarInformacionActionPerformed

    private void BtnLogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLogoActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Seleccionar Logo de la Empresa");
    fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

    // Filtro para solo im√°genes
    javax.swing.filechooser.FileNameExtensionFilter filtro = new javax.swing.filechooser.FileNameExtensionFilter(
        "Im√°genes (JPG, PNG)", "jpg", "jpeg", "png"
    );
    fileChooser.setFileFilter(filtro);

    int seleccion = fileChooser.showOpenDialog(this);
    if (seleccion == JFileChooser.APPROVE_OPTION) {
        File archivoSeleccionado = fileChooser.getSelectedFile();
        String rutaImagen = archivoSeleccionado.getAbsolutePath();

        // Mostrar ruta en txtImagen
        txtImagen.setText(rutaImagen);

        // Escalar y mostrar imagen en JLabel
        ImageIcon icon = new ImageIcon(rutaImagen);
        Image img = icon.getImage().getScaledInstance(
            jLabel_Imagen.getWidth(), jLabel_Imagen.getHeight(), Image.SCALE_SMOOTH
        );
        jLabel_Imagen.setIcon(new ImageIcon(img));
    }
    }//GEN-LAST:event_BtnLogoActionPerformed

